// Logical AND	, OR, NOT
var n1 = 10;
var n2 = 20;
// true && true = true, Other wise false
if(n1 >= 10 &&  n2 <= 20){
    console.log("True...AND");
}

// false || false = false, Other wise true
if(n1 >= 100 ||  n2 <= 20){
    console.log("True...OR");
}

//  !false = true, !true = false
if(!0){
    console.log("True...(0)");
}

