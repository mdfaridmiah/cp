// Increment
console.log("Increment:");
var a = 0;
console.log("L1a : "+ a);
a++;
console.log("L2a : "+ a);
console.log("L3a : "+ a++);
console.log("L4a : "+ a);
console.log("L5a : "+ ++a);


// Decrement
console.log("Decrement:");
var b = 10;
console.log("L1a : "+ b);
b--;
console.log("L2b : "+ b);
console.log("L3b : "+ b--);
console.log("L4b : "+ b);
console.log("L5b : "+ --b);